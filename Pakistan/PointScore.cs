using System;
namespace Pakistan
{
    class PointScore
    {
        private int x;
        private int y;
        public PointScore(int x, int y)
        {
            this.x = x;
            this.y = y;
        }
        public int getX()
        {
            return this.x;
        }
        public int getY()
        {
            return this.y;
        }

    }
}
